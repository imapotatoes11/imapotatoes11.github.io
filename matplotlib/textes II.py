import numpy as np
import matplotlib.pyplot as plt
x=np.random.beta(5.0,1.0,10000)
y=np.random.beta(5.0,1.0,10000)
plt.scatter(x,y,s=0.5)
plt.title('beta distribution')
plt.show()

